def intToOrdinal(integer):
    """
    convert and integer between 1 and 12 to ordinal
    """
    my_dict = {
            1:"first",
            2:"second",
            3:"third",
            4:"fourth",
            5:"fifth",
            6:"sixth",
            7:"seventh",
            8:"eighth",
            9:"ninth",
            10:"tenth",
            11:"eleventh",
            12:"twelth"
            }

    return my_dict[integer]


    
