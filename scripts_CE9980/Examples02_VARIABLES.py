# WHAT IS A VARIABLE

# variable: a name bound to a value (object)
# object:   a value of a particular type
#           with charcteristic behavior
#           3 object types:
#           float ,int and str (atomic object types) only ones used in core 
#           python
#           compute data and process text
# function: a 'macro' that does some processing
#           (marked by a name followed by parentheses)
# argument: an object or objects 'passed' to a function (placed within
#           parentheses)
# call:     when a function is activated (happens when python sees the 
#           parentheses)
# return value: the value that 'comes back' or results from a function call
# Note: the word Class is a synanym for word Type   
